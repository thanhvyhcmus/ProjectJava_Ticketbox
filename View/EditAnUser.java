            radio_admin.setSelected(true);
        fullname.setText(user.getFullname());
        String[] dateofbirth = user.getDoB().split(" ")[0].split("-");
        //System.out.println(dateofbirth[1]);
        int y = Integer.parseInt(dateofbirth[0]);
        int m = Integer.parseInt(dateofbirth[1]);
        int d = Integer.parseInt(dateofbirth[2]);
